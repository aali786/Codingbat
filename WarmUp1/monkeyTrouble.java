public boolean monkeyTrouble(boolean aSmile, boolean bSmile) {
  if(aSmile == true && bSmile == true){
    return true;
  }
  else if(aSmile == false && bSmile == false){
    return true;
  }
  else{
    return false;
  }
}
//Or you can use (this for intermediate)
return aSmile == bSmile;
